///////////////////////////////////////////////////////////////////////////////////////
// Ошибки выдаются потому что функции используют путь, который уже изменен. 
///////////////////////////////////////////////////////////////////////////////////////
// Даже функция изменения названии файла работает основываясь на пути, и после того как
//Она изменила файл при повторном запуске она выдаст ошибку т.к. исходного файла по исходному
//пути уже нет
const fs = require('fs')
const path = require('path')
///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////
fs.writeFile(path.join(__dirname, 'notes', 'mynote.txt'),
'Hello world!', (err) =>{
 	if(err) throw err
console.log('файл создан')
})
///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////
fs.appendFile(path.join(__dirname, 'notes', 'mynote.txt'),
' Новый текст с функции appendFile', (err) =>{
 	if(err) throw err
console.log('файл изменен')
})
///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////
fs.rename(
    path.join(__dirname, 'notes', 'mynote.txt'),
    path.join(__dirname, 'notes', 'awesome_notes.txt'),
(err) =>{
 	if(err) throw err
console.log("Файл переименор")
})
///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////
fs.readFile(path.join(__dirname, 'notes', 'mynote.txt'),
'utf-8',
(err, data) =>{
 	if(err) throw err
console.log(data)
})
